<script type="text/javascript">

$('#SubmitForm').on('submit',function(e){
    e.preventDefault();

    let Title = $('#Title').val();
    let URL = $('#URL').val();
  
    
    $.ajax({
      url: "{{ url('/shorturl')}}",
      type:"POST",
      data:{
        "_token": "{{ csrf_token() }}",
        Title:Title,
       URL:URL,
       
      },
      success:function(response){
        $('#successMsg').show();
        console.log(response);
      },
      error: function(response) {
        $('#nameErrorMsg').text(response.responseJSON.errors.Title);
        $('#emailErrorMsg').text(response.responseJSON.errors.URL);
       
      },
      });
    });
  </script>

